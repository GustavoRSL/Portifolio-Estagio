
function funcao(arg){
    switch(arg){
        case 1:
            console.log("Arg é 1");
            break;
        case 2:
            console.log("Arg é 2");
            break;
        case 3:
            console.log("Arg é 3");
            break;
        default:
            console.log("Arg é algum outro valor diferente de 1,2 e 3.");
            break;
    }
}

funcao(1);
funcao(2);
funcao(3);
funcao(4);