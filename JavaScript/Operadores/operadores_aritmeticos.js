/* 

Tipos relacionais
- == Igual
- != Diferente

- > Maior que
- < Menor que
- >= Maior igual
- <= Menor igual

Tipos relacionais a tipo dos dados
- === Igual e mesmo tipo
- !== Diferente mas mesmo tipo

*/

var mulher = "Maria"
var homem = "Mario"

console.log(1 === '1');
console.log(1 ==  '1');
console.log(mulher !==  homem);

